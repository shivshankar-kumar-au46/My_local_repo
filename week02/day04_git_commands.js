//  1. Git pull :	 This command is used to fetch and download content from Github to my local PC.
// Synatx:  git pull <options>

// 2. Git push:	 This command is used to push the files/directories from local PC to Github account.
//  Synatx:  git push –set-upstream  origin  <branch name>

//  3. Git log :	 This command is used to show a list of all the commits made to a repository.
//  Synatx:  git log

// 4. Git status:	 This command is used to show the files are staged(tracked) or unstaged (untracked).
//  Synatx:  git status

//  5. Git commit : 	 This command is used to save  changes to the local repository.
//  Synatx:  git commit –m “content”

// 6. Git clone :	 This command is used to clone/copy a repository from github account to local PC. 
// Synatx:  git clone <url of git hub repository>

// 7. Git checkout:	The Git checkout command lets navigate between the branches created by git branch.
