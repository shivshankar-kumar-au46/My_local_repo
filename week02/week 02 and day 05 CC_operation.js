//Q-1) Write a program to show the use of +, -, *, / on numbers, in javascript.

// A program to show the use of  +  on numbers, in javascript.

var num1 = parseInt(prompt("Enter first number : "));
var num2 = parseInt(prompt("Enter second number : "));
var sumNum = num1 + num2;
console.log("The sum of",num1,"and",num2,"is : ",sumNum);

// A program to show the use of  -  on numbers, in javascript.

var num1 = parseInt(prompt("Enter first number : "));
var num2 = parseInt(prompt("Enter second number : "));
var sub = num1 - num2;
console.log("The subtraction of",num1,"and",num2,"is : ",sub);

